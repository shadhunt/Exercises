
public class Monitor {
	private String type;
	private String deviceId;
	private String name;
	private boolean incident;
	private String defaultMonitor;
	private String defaultAlarm;
	private String windowId;
	private String viewerType;
	private String wallController;
	private String compatibleFormats;
	private String videoStreamSupported;
	private String directVideoSupported;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean getIncident() {
		return incident;
	}
	public void setIncident(boolean incident) {
		this.incident = incident;
	}
	public String getDefaultMonitor() {
		return defaultMonitor;
	}
	public void setDefaultMonitor(String defaultMonitor) {
		this.defaultMonitor = defaultMonitor;
	}
	public String getDefaultAlarm() {
		return defaultAlarm;
	}
	public void setDefaultAlarm(String defaultAlarm) {
		this.defaultAlarm = defaultAlarm;
	}
	public String getViewerType() {
		return viewerType;
	}
	public void setViewerType(String viewerType) {
		this.viewerType = viewerType;
	}
	public String getWallController() {
		return wallController;
	}
	public void setWallController(String wallController) {
		this.wallController = wallController;
	}
	public String getCompatibleFormats() {
		return compatibleFormats;
	}
	public void setCompatibleFormats(String compatibleFormats) {
		this.compatibleFormats = compatibleFormats;
	}
	public String getVideoStreamSupported() {
		return videoStreamSupported;
	}
	public void setVideoStreamSupported(String videoStreamSupported) {
		this.videoStreamSupported = videoStreamSupported;
	}
	public String getDirectVideoSupported() {
		return directVideoSupported;
	}
	public void setDirectVideoSupported(String directVideoSupported) {
		this.directVideoSupported = directVideoSupported;
	}
	public String getWindowId() {
		return windowId;
	}
	public void setWindowId(String windowId) {
		this.windowId = windowId;
	}
	
}
